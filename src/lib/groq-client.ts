// ─── Rate limit tracker ───────────────────────────────────────────────────────

const rateLimitedUntil: Record<string, number> = {};
const RATE_LIMIT_COOLDOWN_MS = 65_000;

function getAvailableModels() {
  const now = Date.now();
  return GROQ_MODELS.filter((m) => (rateLimitedUntil[m.id] ?? 0) < now);
}

function markRateLimited(modelId: string) {
  rateLimitedUntil[modelId] = Date.now() + RATE_LIMIT_COOLDOWN_MS;
  console.warn(`[Groq] Модель ${modelId} rate-limited, заблокирована на ${RATE_LIMIT_COOLDOWN_MS / 1000}s`);
}

function getGroqClient() {
  const apiKey = process.env.GROQ_API_KEY;
  if (!apiKey) throw new Error('GROQ_API_KEY не задан в .env.local');
  return new Groq({ apiKey });
}

function isRateLimitError(err: unknown): boolean {
  if (err instanceof Groq.RateLimitError) return true;
  if (err && typeof err === 'object' && 'status' in err && (err as { status: number }).status === 429) return true;
  const msg = String(err).toLowerCase();
  return msg.includes('rate limit') || msg.includes('429') || msg.includes('rate_limit');
}

function isModelUnavailableError(err: unknown): boolean {
  if (err && typeof err === 'object' && 'status' in err) {
    const status = (err as { status: number }).status;
    if (status === 404 || status === 400 || status === 503) return true;
  }
  const msg = String(err).toLowerCase();
  return msg.includes('model') && (
    msg.includes('not found') || msg.includes('unavailable') ||
    msg.includes('decommissioned') || msg.includes('does not exist') || msg.includes('no access')
  );
}

// ─── Streaming ────────────────────────────────────────────────────────────────

export async function* analyzeWithGroqStream(prompt: string): AsyncGenerator<string> {
  const groq = getGroqClient();
  const systemPrompt = buildSystemPrompt();

  let lastError: unknown = null;

  for (let attempt = 0; attempt < 2; attempt++) {
    const available = getAvailableModels();

    if (available.length === 0) {
      if (attempt === 0) {
        console.warn('[Groq] Все модели rate-limited, ждём 10s...');
        await new Promise((r) => setTimeout(r, 10_000));
        continue;
      }
      break;
    }

    for (const model of available) {
      try {
        console.log(`[Groq] Пробую модель: ${model.label} (${model.id})`);

        const stream = await groq.chat.completions.create({
          model: model.id,
          messages: [
            { role: 'system', content: systemPrompt },
            { role: 'user',   content: prompt },
          ],
          temperature: 0.3,
          max_tokens: Math.min(model.maxTokens, 8192),
          stream: true,
        });

        yield `\n> *Анализирует: ${model.label}*\n\n`;

        for await (const chunk of stream) {
          const text = chunk.choices[0]?.delta?.content;
          if (text) yield text;
        }

        return;

      } catch (err) {
        lastError = err;

        if (isRateLimitError(err)) {
          markRateLimited(model.id);
        } else if (isModelUnavailableError(err)) {
          rateLimitedUntil[model.id] = Date.now() + 24 * 60 * 60 * 1000;
          console.warn(`[Groq] Модель ${model.id} недоступна, исключаем`);
        } else {
          console.warn(`[Groq analyze] ${model.id} ошибка:`, err);
        }
        continue;
      }
    }
  }

  throw new Error(`Groq: все модели недоступны или rate-limited. Последняя ошибка: ${lastError}`);
}

